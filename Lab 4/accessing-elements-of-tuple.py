# How to access the elements of Tuple

my_tuple = ('batman', True, 911, 'superman', ['ali', 'umar', 'aqib'])

print(my_tuple[2]) # postive indexing
print(my_tuple[-2]) # negtive indexing
print(my_tuple[3][2]) # mested indexing
print(my_tuple[1:4]) # slicing operator
print(my_tuple[:4]) # slicing operator without starting point
print(my_tuple[:])