# Operations of Tuple

# Concatenation (+)

my_tuple1 = (1, 2, 3)
my_tuple2 = (5, 6, 7)
my_tuple3 = my_tuple1 + my_tuple2 # concatenation

# Repeatition (*)

my_tuple4 = my_tuple1 * 3
print(my_tuple4)

