# Tuple is an immutable (unchangeable) collection of same or different datatype variables. 

# Syntax

my_tuple1 = (1, 2, 3, 4, 5) # same datatype
my_tuple2 = (1, 2, "Books", ['usman', 'mahender', 'samad']) # different datatypes
my_tuple3 = () # empty tuple
my_tuple4 = 1, 2, 3
my_tuple5 = 1,

print(type(my_tuple1))
print(type(my_tuple4))
print(type(my_tuple5))

