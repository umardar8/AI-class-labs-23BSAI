# Sets are collection of unordered and unique elements

# Syntax

my_set = {'ali', 'ali', 'saif', 1, 2, 3}

print(my_set)

# Operations on Sets

# Union

my_set1 = {1, 2, 3}
my_set2 = {2, 3, 4, 5, 6}

print(my_set1.union(my_set2))

# Intersection

print(my_set1.intersection(my_set2))

# Difference

print(my_set1.difference(my_set2))